#include <stdio.h>
#include <string.h>
#include <stdlib.h>

int	ft_strlen(const char *str)
{
	int	cont;
	int	num_char;

	cont = 0;
	num_char = 0;
	while (str[cont] != '\0')
	{
		num_char++;
		cont++;
	}
	return (num_char);
}

static int	ft_char_in_set(char c, char const *set)
{
	size_t	i;

	i = 0;
	while (set[i])
	{
		if (set[i] == c)
			return (1);
		i++;
	}
	return (0);
}

char	*ft_strtrim(char const *s1, char const *set)
{
	char	*str;
	size_t	i;
	size_t	start;
	size_t	end;

	start = 0;
	while (s1[start] && ft_char_in_set(s1[start], set))
		start++;
	end = ft_strlen(s1);
	while (end > start && ft_char_in_set(s1[end - 1], set))
		end--;
	str = (char *)malloc(sizeof(*s1) * (end - start + 1));
	if (!str)
		return (NULL);
	i = 0;
	while (start < end)
		str[i++] = s1[start++];
	str[i] = 0;
	return (str);
}

int	main(void)
{
	char	*nueva;

	nueva = ft_strtrim("cadena nueva de cadena", "cadena");
	printf("%s\n", nueva);
}
